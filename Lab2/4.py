# Написати програму, що визначає щасливий квиток
# Щасливий квиток - той, чия сума перших 3 цифр = сумі останніх 3

print("Введіть шестизначний номер:\n")

n = int(input("№: "))  # номер (шестизначний)
# відбувається перевірка:
if ((n // 100000) + (n // 10000 % 10) + (n // 1000 % 10)) == ((n // 100 % 10) + (n // 10 % 10) + n % 10):
    print("\nЩасливий")
else:
    print("\nНе той квиток тобі продали :/")
