#Дыапазон: 1900-3000

print("Укажите интересующий Вас год (в диапазоне 1900-3000)\n")

year = int(input("Год: "))

if year < 1900 and year > 3000:
	print("\nЗа пределами диапазона")
elif year % 4 == 0 and year % 100 != 0 or year % 400 == 0:
	print("\nВысокосный")
else:
	print("\nОбычный")